_DOCUMENT_INDEX = None


def _ensure_document_index():
    global _DOCUMENT_INDEX
    if _DOCUMENT_INDEX is None:
        _DOCUMENT_INDEX = {}
        for purpose in DOCUMENT_PURPOSES.values():
            for doc in purpose["documents"]:
                _DOCUMENT_INDEX[doc["id"]] = doc
        _DOCUMENT_INDEX.update(EXTRA_DOCUMENTS)
    return _DOCUMENT_INDEX


def get_documents_for_division(division):
    doc_index = _ensure_document_index()
    document_ids = DIVISION_DOCUMENTS.get(division, [])
    return [doc_index[doc_id] for doc_id in document_ids if doc_id in doc_index]


def get_purpose_options(language):
    return [
        {
            "id": f"purpose:{key}",
            "label": value["label_en"] if language == "en" else value["label_ms"],
            "key": key,
        }
        for key, value in DOCUMENT_PURPOSES.items()
    ]


def get_documents_for_purpose(purpose_key):
    return DOCUMENT_PURPOSES.get(purpose_key, {}).get("documents", [])


def get_purpose_label(purpose_key, language):
    purpose = DOCUMENT_PURPOSES.get(purpose_key)
    if not purpose:
        return ""
    return purpose["label_en"] if language == "en" else purpose["label_ms"]


def get_purpose_copy(purpose_key, language):
    purpose = DOCUMENT_PURPOSES.get(purpose_key, {})
    if language == "en":
        return {
            "title": purpose.get("title_en", "Recommendations"),
            "description": purpose.get("description_en", ""),
        }
    return {
        "title": purpose.get("title_ms", "Cadangan"),
        "description": purpose.get("description_ms", ""),
    }
